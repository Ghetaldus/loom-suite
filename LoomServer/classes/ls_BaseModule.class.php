abstract class ls_BaseModule {

	//====================================================================================
	//										PROPERTIES
	//====================================================================================

	protected $initOnAwake 	= false;
	protected $core 		= null;
	
	protected function __clone() {}
	
	protected function initalize() {}
	
	//====================================================================================
	//										METHODS
	//====================================================================================
	
	//------------------------------------------------------------------------------------
	// __construct
	//------------------------------------------------------------------------------------
	public function __construct(ls_Core&$core) {
		$this->core = $core;
		if ($this->initOnAwake)
			$this->initalize();
	}
	
	// -----------------------------------------------------------------------------------

}

