using loom;
using UnityEngine;
using UnityEngine.UI;

namespace loom {

	// ===================================================================================
	// LC_UIPanelOptions
	// ===================================================================================
	public class LC_UIPanelOptions : LC_UIPanel {
		
		[SerializeField] Button buttonQuit;
		
		//--------------------------------------------------------------------------------
		// OnChildEnable
		//--------------------------------------------------------------------------------
		public override void OnChildEnable() {}
		
	   	//--------------------------------------------------------------------------------
		// onClickChangePassword
		//--------------------------------------------------------------------------------		
		public void onClickChangePassword() {
			Hide();
			FindObjectOfType<LC_UIPanelChangePassword>().Show();
		}
		
		//--------------------------------------------------------------------------------
		// OnInvokeRepeating
		//--------------------------------------------------------------------------------
		public override void OnInvokeRepeating() {}

	   	//--------------------------------------------------------------------------------
		// onClickChangeEmail
		//--------------------------------------------------------------------------------		
		public void onClickChangeEmail() {
			Hide();
			FindObjectOfType<LC_UIPanelChangeEmail>().Show();
		}
		
	   	//--------------------------------------------------------------------------------
		// onClickChangeName
		//--------------------------------------------------------------------------------		
		public void onClickChangeName() {
			Hide();
			FindObjectOfType<LC_UIPanelChangeName>().Show();
		}
		
	   	//--------------------------------------------------------------------------------
		// onClickDeleteAccount
		//--------------------------------------------------------------------------------		
		public void onClickDeleteAccount() {
			Hide();
			FindObjectOfType<LC_UIPanelDeleteAccount>().Show();
		}
		
		//--------------------------------------------------------------------------------
		// onClickAdmin
		//--------------------------------------------------------------------------------		
		public void onClickAdmin() {
		
		}
		
		//--------------------------------------------------------------------------------
		// onClickQuit
		//--------------------------------------------------------------------------------		
		public void onClickQuit() {
			TemporaryDisable(buttonQuit);
			Application.Quit();
		}
		
		//--------------------------------------------------------------------------------
		// onClickClose
		//--------------------------------------------------------------------------------		
		public void onClickClose() {
			Hide();
		}
		
	   	//--------------------------------------------------------------------------------
	    
	}

}

